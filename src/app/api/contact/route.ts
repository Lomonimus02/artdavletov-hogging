import { NextRequest, NextResponse } from 'next/server';
import axios from 'axios';

interface ContactFormData {
  name: string;
  email: string;
  phone?: string;
  message: string;
  source: 'home' | 'contacts';
}

export async function POST(request: NextRequest) {
  try {
    const body: ContactFormData = await request.json();
    const { name, email, phone, message, source } = body;

    // –í–∞–ª–∏–¥–∞—Ü–∏—è –¥–∞–Ω–Ω—ã—Ö
    if (!name || !email || !message) {
      return NextResponse.json(
        { error: '–û–±—è–∑–∞—Ç–µ–ª—å–Ω—ã–µ –ø–æ–ª—è: –∏–º—è, email, —Å–æ–æ–±—â–µ–Ω–∏–µ' },
        { status: 400 }
      );
    }

    // –ü–æ–ª—É—á–∞–µ–º –ø–µ—Ä–µ–º–µ–Ω–Ω—ã–µ –æ–∫—Ä—É–∂–µ–Ω–∏—è
    const botToken = process.env.TELEGRAM_BOT_TOKEN;
    const chatId = process.env.TELEGRAM_CHAT_ID;

    if (!botToken || !chatId) {
      console.error('Telegram bot token –∏–ª–∏ chat ID –Ω–µ –Ω–∞—Å—Ç—Ä–æ–µ–Ω—ã');
      return NextResponse.json(
        { error: '–°–µ—Ä–≤–∏—Å –≤—Ä–µ–º–µ–Ω–Ω–æ –Ω–µ–¥–æ—Å—Ç—É–ø–µ–Ω' },
        { status: 500 }
      );
    }

    // –§–æ—Ä–º–∏—Ä—É–µ–º —Å–æ–æ–±—â–µ–Ω–∏–µ –¥–ª—è Telegram
    const sourceText = source === 'home' ? '–ì–ª–∞–≤–Ω–∞—è —Å—Ç—Ä–∞–Ω–∏—Ü–∞' : '–°—Ç—Ä–∞–Ω–∏—Ü–∞ –∫–æ–Ω—Ç–∞–∫—Ç–æ–≤';
    const phoneText = phone ? `\nüìû –¢–µ–ª–µ—Ñ–æ–Ω: ${phone}` : '';
    
    const telegramMessage = `üèóÔ∏è –ù–æ–≤–æ–µ —Å–æ–æ–±—â–µ–Ω–∏–µ —Å —Å–∞–π—Ç–∞!

üìç –ò—Å—Ç–æ—á–Ω–∏–∫: ${sourceText}
üë§ –ò–º—è: ${name}
üìß Email: ${email}${phoneText}

üí¨ –°–æ–æ–±—â–µ–Ω–∏–µ:
${message}

‚è∞ –í—Ä–µ–º—è: ${new Date().toLocaleString('ru-RU', { timeZone: 'Europe/Moscow' })}`;

    // –û—Ç–ø—Ä–∞–≤–ª—è–µ–º —Å–æ–æ–±—â–µ–Ω–∏–µ –≤ Telegram
    const telegramApiUrl = `https://api.telegram.org/bot${botToken}/sendMessage`;
    
    await axios.post(telegramApiUrl, {
      chat_id: chatId,
      text: telegramMessage,
      parse_mode: 'HTML'
    });

    return NextResponse.json(
      { message: '–°–æ–æ–±—â–µ–Ω–∏–µ —É—Å–ø–µ—à–Ω–æ –æ—Ç–ø—Ä–∞–≤–ª–µ–Ω–æ!' },
      { status: 200 }
    );

  } catch (error) {
    console.error('–û—à–∏–±–∫–∞ –ø—Ä–∏ –æ—Ç–ø—Ä–∞–≤–∫–µ —Å–æ–æ–±—â–µ–Ω–∏—è:', error);
    
    // –ü—Ä–æ–≤–µ—Ä—è–µ–º, —è–≤–ª—è–µ—Ç—Å—è –ª–∏ —ç—Ç–æ –æ—à–∏–±–∫–æ–π Telegram API
    if (axios.isAxiosError(error) && error.response) {
      console.error('Telegram API error:', error.response.data);
    }

    return NextResponse.json(
      { error: '–ü—Ä–æ–∏–∑–æ—à–ª–∞ –æ—à–∏–±–∫–∞ –ø—Ä–∏ –æ—Ç–ø—Ä–∞–≤–∫–µ —Å–æ–æ–±—â–µ–Ω–∏—è' },
      { status: 500 }
    );
  }
}
